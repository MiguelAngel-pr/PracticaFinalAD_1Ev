    private void botonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirActionPerformed
        //Botón con el que se añaden todos los elementos y atributos que forman la nueva películas
        //Se permite añadir texto vacio en los campos no obligatorios
        String titulo = respuestaTitulo.getText();
        String director = respuestaDirector.getText();
        String actor1 = respuestaActor1.getText();
        String actor2 = respuestaActor2.getText();
        String compositor = respuestaCompositor.getText();
        String año = respuestaAño.getValue().toString();
        String edad = respuestaEdad.getValue().toString();
        String genero1 = respuestaGenero1.getSelectedItem().toString();
        String genero2 = respuestaGenero2.getSelectedItem().toString();
        String pais = respuestaPais.getText();
        
        //Validación
        if(comprobarLongitud(titulo,director,pais) && comprobarTexto(director, actor1, actor2, compositor, pais) && !genero1.equals(genero2) 
           && ventana.dom.añadirDOM(titulo, director, edad, genero1, genero2, año, actor1,actor2, compositor, pais) == 0)
        {
            this.dispose();
            ventana.asignaTexto();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "El texto no se ha introducido correctamente");
        }
    }//GEN-LAST:event_botonAñadirActionPerformed

