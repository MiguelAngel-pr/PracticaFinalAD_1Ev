    public boolean comprobarTexto(String a0,String a1, String a2, String a3, String a4)//Metodo usado para impedir que escriban números en el nombre
    {
        for(int i = 0; i<10; i++)
        {
            if((a0).contains(""+i)||(a1).contains(""+i)||(a2).contains(""+i)||(a3).contains(""+i)||(a4).contains(""+i))
            {
                return false;
            }
        }
        return true;
    }
    
    public boolean comprobarLongitud(String texto1, String texto2, String texto3)//Método usado para comprobar que la longitud cumple los requisitos
    {
        String datos[]= new String[] {texto1,texto2,texto3};
        for(int i=0; i<3; i++)
        {
            if(datos[i].length() < 3 || datos[i].length() > 25)
            {
                return false;
            }
        } 
        return true;
    }
